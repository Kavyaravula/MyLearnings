package collections;

public class Employee {
	String empName;
	int empNo;
	String address;
	int age;
	String dept;
	
	
	


Employee (String name, int no, String add, int age, String dept){
	
	this.empName= name;
	this.empNo= no;
	this.address= add;
	this.age= age;
	this.dept= dept;
	
}

}